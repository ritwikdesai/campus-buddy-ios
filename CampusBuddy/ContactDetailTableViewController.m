- (void)viewDidLoad
{
    [super viewDidLoad];

    self.title = self.subCategory.subCategoryName;
    self.tableView.dataSource = self;
    self.tableView.delegate = self;
   
    DatabaseHelper* helper =[DatabaseHelper getDatabaseHelper];
    
